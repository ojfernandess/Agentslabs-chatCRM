type Nested = typeof messages["pt-BR"];

function getNested(obj: Nested, path: string): string {
  const parts = path.split(".");
  let cur: unknown = obj;
  for (const p of parts) {
    if (cur !== null && typeof cur === "object" && p in cur) {
      cur = (cur as Record<string, unknown>)[p];
    } else {
      return path;
    }
  }
  return typeof cur === "string" ? cur : path;
}

export function translate(locale: LocaleCode, path: string): string {
  return getNested(messages[locale] as unknown as Nested, path);
}
